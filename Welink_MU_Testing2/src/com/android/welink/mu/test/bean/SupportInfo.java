package com.android.welink.mu.test.bean;

public class SupportInfo
{
	private int codecMode;
	private int touchEvent;
	private int totalFrame;
	private int successFrame;
	private int errorFrame;
	
	public int getCodecMode() {
		return codecMode;
	}
	public void setCodecMode(int codecMode) {
		this.codecMode = codecMode;
	}
	public int getTouchEvent() {
		return touchEvent;
	}
	public void setTouchEvent(int touchEvent) {
		this.touchEvent = touchEvent;
	}
	public int getTotalFrame() {
		return totalFrame;
	}
	public void setTotalFrame(int totalFrame) {
		this.totalFrame = totalFrame;
	}
	public int getSuccessFrame() {
		return successFrame;
	}
	public void setSuccessFrame(int successFrame) {
		this.successFrame = successFrame;
	}
	public int getErrorFrame() {
		return errorFrame;
	}
	public void setErrorFrame(int errorFrame) {
		this.errorFrame = errorFrame;
	}
	
	
}
